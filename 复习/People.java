package 复习;

public class People {
    // 成员变量
    String name;
    int age;
    String weight;
    int height;
    String sex;

    // 成员方法
    public static void Name(String n){
        System.out.println("姓名：" + n);
    }

    public static void Age(int a){
        System.out.println("年龄：" + a);
    }

    public static void Weight(String w){
        System.out.println("体重：" + w);
    }

    public static void Height(int h){
        System.out.println("身高：" + h);
    }

    public static void Sex(String s){
        System.out.println("性别：" + s);
    }
}
