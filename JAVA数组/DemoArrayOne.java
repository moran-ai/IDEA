package JAVA数组;

public class DemoArrayOne {
    public static void main(String[] args) {
        // 动态初始化数组
        int[] array = new int[3];
        System.out.println(array[0]);
        System.out.println("================");
        // 改变数组中的内容
        array[0] = 2;
        System.out.println(array[0]);

    }
}
